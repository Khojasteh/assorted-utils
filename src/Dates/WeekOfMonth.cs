namespace Assorted.Utils.Dates
{
    /// <summary>
    /// Represents weeks of the month.
    /// </summary>
    public enum WeekOfMonth
    {
        /// <summary>
        /// The first week of the month.
        /// </summary>
        First = 1,

        /// <summary>
        /// The second week of the month.
        /// </summary>
        Second = 2,

        /// <summary>
        /// The third week of the month.
        /// </summary>
        Third = 3,

        /// <summary>
        /// The fourth week of the month.
        /// </summary>
        Fourth = 4,

        /// <summary>
        /// The last week of the month, which can be either the fourth or the fifth week.
        /// </summary>
        Last = 5
    }
}
